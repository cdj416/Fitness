package com.hongyuan.fitness.custom_view.share_view;

public class ShareBeans {

    private String shareImgUrl;
    private String shareWebsite;
    private String shareTitle;
    private String shareInfo;

    public String getShareImgUrl() {
        return shareImgUrl;
    }

    public void setShareImgUrl(String shareImgUrl) {
        this.shareImgUrl = shareImgUrl;
    }

    public String getShareWebsite() {
        return shareWebsite;
    }

    public void setShareWebsite(String shareWebsite) {
        this.shareWebsite = shareWebsite;
    }

    public String getShareTitle() {
        return shareTitle;
    }

    public void setShareTitle(String shareTitle) {
        this.shareTitle = shareTitle;
    }

    public String getShareInfo() {
        return shareInfo;
    }

    public void setShareInfo(String shareInfo) {
        this.shareInfo = shareInfo;
    }
}
